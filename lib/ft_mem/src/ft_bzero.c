#include "ft_mem.h"

void		ft_bzero(void *s, size_t n)
{
	u_char	*ss;
	size_t	i;

	if (n == 0)
		return ;
	i = 0;
	ss = (u_char *)s;
	while (i < n)
		ss[i++] = 0;
}
